public class Vowelorconsonant {
    public static void main(String[] args) {
        char ch= 'R';
        switch(ch){
        case 'A':
        case 'E':
        case 'I':
        case 'O':
        case 'U':
         System.out.println("vowel");
        break;
        default:
        System.out.println("consonant");
        }
    }
}         
          
        
    
    

