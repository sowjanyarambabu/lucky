public class Relational {
    public static void main(String[] args) {
       int c =20; 
       int d =5;
       System.out.println(c==d);
       System.out.println(c!=d);
       System.out.println(c<d);
       System.out.println(c<=d);
       System.out.println(c>d);
       System.out.println(c>=d);
    }
}
